from discrete_optimization.generic_tools.do_solver import StatusSolver
from discrete_optimization.singlemachine.solvers.optal import OptalSingleMachineSolver


def test_optal(problem):
    solver = OptalSingleMachineSolver(problem=problem)
    solver.init_model()
    res = solver.solve(
        time_limit=2,
    )
    assert solver.status_solver in (StatusSolver.OPTIMAL, StatusSolver.SATISFIED)
